!===============================================================================

subroutine print_version(unit)
	use iso_fortran_env
	integer, optional, intent(in) :: unit
	!********
	integer :: unit_

	unit_ = output_unit
	if (present(unit)) unit_ = unit

	write(unit_, "(a)") " numa version " // &
		to_str(NUMA_MAJOR) // "." // &
		to_str(NUMA_MINOR) // "." // &
		to_str(NUMA_PATCH)

end subroutine print_version

